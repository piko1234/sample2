package loops;

public class Do_while2 
{

	public static void main(String[] args)
	{
       System.out.println("======================");
		
		int i=1;
		do 
		{
			System.out.println("cube of " +i+" is "+i*i*i);
			i++;
		}  
		while (i<=10);
		
		System.out.println("======================");
		
		int j=2;
		do 
		{
			System.out.println("square of " +j+" is "+j*j);
			j++;
			
		} while (j<=10);
	System.out.println("========================");	
	}
	

}
