package loops;

public class For_loop
{

	public static void main(String[] args)
	{
      //want to print 1 five times
	 //from where to start --> initialization 1
	 //condition <=5
	//1,2,3,4,5--> incrementing , 5,4,3,2,1 --> decrementing
		
		int i;
		for(i=1;i<=5;i++)
		{
			System.out.println("1");
			
		}
		
		for(i=5;i>=1;i--)
		{
		    
			System.out.println("one");	
			
		}
		
		
		
	}

}
