package loops;

public class Loop_tables 
{
	public static void main(String[] args) 
{
		System.out.println("Table of 3");
	for(int i=3;i<=30;i=i+3)	
     {
	       System.out.println(i);	
     }
	 
	System.out.println("\nTable of 4");
	for (int i=1;i<=10;i++)
	{
		System.out.println(i*4);
		
	}
	}


}
