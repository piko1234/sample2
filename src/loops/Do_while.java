package loops;

public class Do_while
{

	public static void main(String[] args)
	{
		//I want to print velocity 3 times
		
		
		int i=1;
		do 
		{
         System.out.println("velocity");
         i++;
		}
		while(i<=3);
		
		
		int j=3;
		
		do 
		{
			System.out.println("smart study");
			j--;
		}
		while(j>=1);
	
		
		int v=5;
		
		do
		{
			System.out.println(v);
			v--;
		}
		while(v>0);
	}

}
